export type Language = "vi" | "en";
export type TranslationKeys = keyof typeof translations.vi;

interface LanguageContextType {
    language: Language;
    setLanguage: (lang: Language) => Promise<void>;
    t: (key: TranslationKeys, replaceObj?: Record<string, string | number>) => string;
}

const LanguageContext = createContext<LanguageContextType | undefined>(undefined);

export function LanguageProvider({ children }: { children: React.ReactNode }) {
    const [language, setLanguageState] = useState<Language>("vi");

    useEffect(() => {
        AsyncStorage.getItem("mievoh_language").then((stored) => {
            if (stored === "vi" || stored === "en") {
                setLanguageState(stored);
            }
        });
    }, []);

    const setLanguage = async (lang: Language) => {
        setLanguageState(lang);
        await AsyncStorage.setItem("mievoh_language", lang);
    };

    const t = (key: TranslationKeys, replaceObj?: Record<string, string | number>): string => {
        const langData = translations[language];
        let text = (langData && langData[key]) || key;
        
        if (replaceObj) {
            Object.entries(replaceObj).forEach(([k, v]) => {
                text = text.replace(new RegExp(`{${k}}`, "g"), String(v));
            });
        }
        return text;
    };

    return (
        <LanguageContext.Provider value={{ language, setLanguage, t }}>
            {children}
        </LanguageContext.Provider>
    );
}

export function useLanguage() {
    const context = useContext(LanguageContext);
    if (!context) {
        throw new Error("useLanguage must be used within a LanguageProvider");
    }
    return context;
}
